    <section>
      <h2>1. Declaración de consentimiento</h2>
      <p>Al marcar la casilla correspondiente en el formulario de IRIS, el usuario manifiesta de forma expresa que autoriza a IRIS a tratar sus datos personales sensibles conforme a este consentimiento y al Aviso de Privacidad Integral.</p>
      <p>Este consentimiento se solicita de manera separada porque IRIS puede tratar información relacionada con salud mental, estado emocional, antecedentes psicológicos, seguimiento terapéutico, diario personal, tareas, motivos de consulta y otros aspectos íntimos necesarios para la prestación de servicios relacionados con bienestar y atención psicológica.</p>
    </section>

    <section>
      <h2>2. Datos sensibles incluidos</h2>
      <p>El consentimiento comprende datos relacionados con salud física y mental, antecedentes psicológicos o psiquiátricos, síntomas, emociones, crisis, riesgo de autolesión o suicidio, medicación referida por el usuario, discapacidad, relaciones familiares, contexto social, experiencias personales, hábitos, vida íntima cuando sea clínicamente relevante, motivo de consulta, respuestas a cuestionarios, notas de seguimiento, diario personal, tareas terapéuticas, contacto de emergencia y comunicaciones compartidas con el profesional autorizado.</p>
    </section>

    <section>
      <h2>3. Finalidades autorizadas</h2>
      <p>El usuario autoriza que sus datos sensibles sean tratados para evaluación inicial, orientación, canalización, agendamiento, prestación de atención psicológica, seguimiento, continuidad terapéutica, comunicación con profesionales autorizados, elaboración y resguardo de registros relacionados con el servicio, gestión de tareas, diario personal, prevención de riesgos, activación de protocolos de emergencia cuando exista peligro razonable y cumplimiento de obligaciones legales o profesionales.</p>
    </section>

    <section>
      <h2>4. Acceso a datos sensibles</h2>
      <p>El acceso a datos sensibles debe limitarse al propio usuario, al profesional tratante, a personal de soporte estrictamente necesario, a proveedores que operen bajo obligaciones de seguridad y confidencialidad, y a autoridades, instituciones de salud, contactos de emergencia o representantes legalmente autorizados cuando exista fundamento legal o situación de riesgo.</p>
    </section>

    <section>
      <h2>5. Voluntariedad y consecuencias</h2>
      <p>El otorgamiento de este consentimiento es voluntario. Sin embargo, si el usuario no lo concede, IRIS puede limitar o impedir el uso de funciones que requieren datos sensibles, como atención psicológica, diario, tareas, seguimiento, expediente, crisis, contacto de emergencia o comunicación clínica con profesionales.</p>
    </section>

    <section>
      <h2>6. Revocación</h2>
      <p>El usuario puede revocar este consentimiento mediante los medios de privacidad disponibles en IRIS. La revocación no tiene efectos retroactivos y puede no afectar tratamientos necesarios para cumplir obligaciones legales, conservar evidencia, atender responsabilidades profesionales, proteger derechos o preservar la vida, salud o integridad de una persona.</p>
    </section>

    <section>
      <h2>7. Registro electrónico</h2>
      <p>La aceptación de este consentimiento puede registrarse electrónicamente con fecha, hora, cuenta, versión del documento, dirección IP, dispositivo, navegador y demás datos técnicos necesarios para acreditar la manifestación de voluntad del usuario.</p>
    </section>

