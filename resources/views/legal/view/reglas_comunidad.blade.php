    <section>
      <h2>1. Finalidad</h2>
      <p>La comunidad de IRIS tiene como finalidad permitir interacción respetuosa, apoyo general, intercambio de experiencias, bienestar y convivencia segura. No sustituye atención psicológica individual, diagnóstico, tratamiento médico ni intervención de crisis.</p>
    </section>

    <section>
      <h2>2. Conducta permitida</h2>
      <p>El usuario debe participar con respeto, empatía, veracidad y responsabilidad. Debe evitar compartir información que pueda identificar a terceros, revelar datos clínicos ajenos, exponer a menores, publicar datos de contacto privados o realizar recomendaciones que puedan poner en riesgo la salud de otra persona.</p>
    </section>

    <section>
      <h2>3. Contenido prohibido</h2>
      <p>No se permite publicar amenazas, acoso, discriminación, insultos, contenido sexual explícito, violencia gráfica, instrucciones de autolesión, promoción de suicidio, información médica peligrosa, fraude, spam, publicidad no autorizada, suplantación, datos personales de terceros, material ilícito o contenido que vulnere derechos de autor, privacidad o seguridad.</p>
    </section>

    <section>
      <h2>4. Moderación</h2>
      <p>IRIS puede revisar, ocultar, eliminar, limitar o reportar publicaciones que infrinjan estas reglas, los Términos y Condiciones o la ley. También puede suspender cuentas, restringir funciones o conservar evidencia cuando sea necesario para proteger usuarios, investigar incidentes o cumplir obligaciones legales.</p>
    </section>

    <section>
      <h2>5. Responsabilidad del usuario</h2>
      <p>El usuario es responsable de sus publicaciones, comentarios, archivos y mensajes. La publicación de experiencias personales debe realizarse evitando datos que permitan identificar a otras personas o revelar información sensible innecesaria.</p>
    </section>

