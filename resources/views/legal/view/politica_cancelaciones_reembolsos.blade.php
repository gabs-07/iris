    <section>
      <h2>1. Alcance</h2>
      <p>Esta Política regula los pagos, confirmaciones, cancelaciones, reprogramaciones, inasistencias, retrasos, reembolsos y aclaraciones relacionados con citas, sesiones, suscripciones o servicios contratados a través de IRIS.</p>
    </section>

    <section>
      <h2>2. Información previa al pago</h2>
      <p>Antes de confirmar un pago, IRIS debe mostrar al usuario la información esencial del servicio disponible en la plataforma, incluyendo profesional, modalidad, fecha, hora, precio, cargos aplicables, impuestos cuando correspondan, método de pago, condiciones de cancelación y políticas relacionadas.</p>
    </section>

    <section>
      <h2>3. Confirmación de cita o servicio</h2>
      <p>Una cita o servicio se considera confirmado cuando el sistema registre la reserva y, en su caso, el pago correspondiente. El usuario debe revisar que los datos de fecha, hora, zona horaria, profesional y modalidad sean correctos antes de confirmar.</p>
    </section>

    <section>
      <h2>4. Cancelación por el usuario</h2>
      <p>El usuario puede cancelar o solicitar reprogramación desde las funciones disponibles en IRIS. La procedencia de reembolso, saldo a favor o reprogramación dependerá del tiempo de anticipación, disponibilidad del profesional, condiciones visibles al momento de la contratación y gastos no recuperables generados por la operación del servicio.</p>
    </section>

    <section>
      <h2>5. Inasistencia y retrasos</h2>
      <p>Si el usuario no se presenta a la sesión o ingresa con retraso, el profesional puede considerar la cita como realizada, reducir el tiempo efectivo de atención o permitir reprogramación conforme a su disponibilidad y a las reglas vigentes. La falta de conexión, problemas del dispositivo o ausencia del usuario no generan automáticamente derecho a reembolso.</p>
    </section>

    <section>
      <h2>6. Cancelación por el profesional</h2>
      <p>Si el profesional cancela o no puede prestar el servicio, IRIS podrá facilitar reprogramación, cambio de profesional, saldo a favor o reembolso del monto pagado por el servicio no prestado, según corresponda y conforme al método de pago utilizado.</p>
    </section>

    <section>
      <h2>7. Fallas técnicas</h2>
      <p>Cuando una falla atribuible a IRIS impida de forma sustancial la prestación del servicio confirmado, el usuario podrá solicitar revisión para reprogramación, saldo a favor o reembolso. Cuando la falla sea atribuible a la conexión, dispositivo, permisos, navegador o entorno del usuario, IRIS podrá apoyar técnicamente, pero no garantiza reembolso automático.</p>
    </section>

    <section>
      <h2>8. Reembolsos</h2>
      <p>Los reembolsos aprobados se procesarán mediante el método de pago original o por el medio que IRIS determine viable. Los tiempos de acreditación dependen del banco, procesador de pago, tarjeta, plataforma intermediaria y verificaciones antifraude. Los cargos de terceros, comisiones, impuestos o servicios ya prestados pueden no ser reembolsables cuando así se informe y sea legalmente permitido.</p>
    </section>

    <section>
      <h2>9. Suscripciones</h2>
      <p>Cuando existan suscripciones, el usuario acepta los cargos recurrentes, periodicidad, precio, funciones incluidas y forma de cancelación mostradas antes de contratar. La cancelación de una suscripción evita renovaciones futuras, pero no necesariamente implica devolución de periodos ya iniciados, salvo que la ley, la oferta vigente o IRIS indiquen lo contrario.</p>
    </section>

    <section>
      <h2>10. Aclaraciones y contracargos</h2>
      <p>El usuario debe reportar cargos no reconocidos, duplicados o incorrectos por los medios de soporte de IRIS. IRIS puede solicitar información razonable para verificar la operación. El abuso de contracargos, reclamaciones falsas o desconocimiento indebido de pagos puede generar suspensión de cuenta y acciones legales.</p>
    </section>

