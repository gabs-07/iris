    <section>
      <h2>1. Responsable del tratamiento</h2>
      <p>IRIS es una plataforma digital para la gestión de servicios relacionados con bienestar, orientación y atención psicológica. IRIS, por conducto de su administración y de los medios de contacto habilitados dentro del sistema, es responsable del tratamiento de los datos personales que se recaban, usan, almacenan, consultan, actualizan, transfieren o eliminan con motivo del uso de la plataforma.</p>
      <p>Este Aviso de Privacidad Integral aplica a visitantes, usuarios registrados, pacientes, profesionales de la salud mental, contactos de emergencia, personas que realizan pagos, personas que solicitan soporte y cualquier persona que proporcione datos personales a IRIS.</p>
    </section>

    <section>
      <h2>2. Datos personales que puede tratar IRIS</h2>
      <p>IRIS puede tratar datos de identificación, contacto, autenticación, cuenta, fotografía de perfil, fecha de nacimiento, edad, género, zona horaria, datos de agenda, historial de citas, mensajes, solicitudes de soporte, preferencias de uso, datos técnicos del dispositivo, dirección IP, registros de acceso, comprobantes de pago y datos de facturación cuando sean proporcionados por el usuario o resulten necesarios para operar el servicio.</p>
      <p>Cuando el usuario utilice funciones clínicas, terapéuticas o de seguimiento, IRIS puede tratar datos relacionados con salud física o mental, motivo de consulta, antecedentes psicológicos o psiquiátricos, síntomas, estado emocional, riesgo, crisis, medicación referida por el usuario, notas de seguimiento, diario personal, tareas terapéuticas, respuestas a formularios, objetivos de atención, archivos o comunicaciones compartidas con el profesional autorizado.</p>
      <p>Respecto de profesionales, IRIS puede tratar datos de identificación, contacto, perfil profesional, cédula profesional, experiencia, especialidades, horarios, tarifas, fotografía, datos fiscales, datos bancarios o de pago, documentos de verificación y evaluaciones relacionadas con la prestación del servicio.</p>
    </section>

    <section>
      <h2>3. Datos personales sensibles</h2>
      <p>Los datos relacionados con salud física o mental, vida emocional, antecedentes clínicos, situación familiar, riesgo de autolesión, discapacidad, orientación, hábitos, vida íntima o cualquier información que pueda afectar la esfera más privada del usuario son considerados sensibles cuando revelen aspectos íntimos o puedan dar lugar a discriminación.</p>
      <p>IRIS únicamente tratará datos personales sensibles cuando sean necesarios para la finalidad informada, cuando el usuario los proporcione voluntariamente o cuando exista una base legal aplicable. Para el uso ordinario de funciones de atención psicológica, seguimiento, diario, tareas o expediente, IRIS solicitará consentimiento expreso por separado.</p>
    </section>

    <section>
      <h2>4. Finalidades principales</h2>
      <p>Los datos personales se utilizan para crear, autenticar y administrar cuentas; permitir el acceso a la plataforma; verificar identidad; validar perfiles profesionales; gestionar agendas, citas, pagos, sesiones, mensajes, diario, tareas y seguimiento; facilitar la comunicación entre paciente y profesional; emitir comprobantes; brindar soporte; prevenir fraude, abuso o accesos no autorizados; conservar evidencia de aceptación legal; atender solicitudes de derechos de privacidad; cumplir obligaciones legales, contractuales, fiscales, administrativas, sanitarias y de seguridad; y proteger la vida, salud o integridad de una persona cuando exista una situación de riesgo.</p>
    </section>

    <section>
      <h2>5. Finalidades secundarias</h2>
      <p>Con autorización del usuario, IRIS puede enviar recordatorios, frases motivacionales, contenido de bienestar, encuestas, comunicaciones informativas, novedades, campañas, promociones, invitaciones, recomendaciones de uso o mensajes no indispensables para la prestación directa del servicio.</p>
      <p>El usuario puede negarse a estas finalidades secundarias o retirar su consentimiento desde las opciones del sistema, mediante el enlace de baja incluido en los correos electrónicos o a través de los medios de contacto de IRIS.</p>
    </section>

    <section>
      <h2>6. Transferencias y encargados</h2>
      <p>IRIS puede permitir el acceso o comunicar datos personales a profesionales autorizados por el usuario, proveedores de alojamiento, seguridad, correo electrónico, videollamadas, analítica operativa, soporte técnico, procesamiento de pagos, facturación, asesoría legal, contable o fiscal, y demás terceros que resulten necesarios para operar la plataforma bajo obligaciones de confidencialidad y seguridad.</p>
      <p>IRIS también puede transferir datos a autoridades competentes, instituciones de salud, servicios de emergencia, contactos de emergencia o personas legalmente autorizadas cuando exista obligación legal, requerimiento de autoridad, riesgo para la vida, salud o integridad de una persona, defensa de derechos, prevención de fraude o cumplimiento de una relación jurídica.</p>
    </section>

    <section>
      <h2>7. Conservación de datos</h2>
      <p>Los datos personales se conservan durante el tiempo necesario para cumplir las finalidades informadas, mantener la cuenta, prestar el servicio, atender responsabilidades legales o profesionales, resolver aclaraciones, cumplir obligaciones fiscales, administrativas o sanitarias y conservar evidencia de consentimiento. Cuando los datos dejen de ser necesarios, IRIS podrá bloquearlos, cancelarlos, anonimizarlos o eliminarlos conforme a sus medidas internas y obligaciones aplicables.</p>
    </section>

    <section>
      <h2>8. Seguridad y confidencialidad</h2>
      <p>IRIS adopta medidas administrativas, técnicas y físicas razonables para proteger los datos personales contra daño, pérdida, alteración, destrucción, uso, acceso o tratamiento no autorizado. El acceso a datos sensibles debe limitarse a usuarios autorizados, profesionales tratantes, personal de soporte indispensable y proveedores sujetos a confidencialidad.</p>
      <p>Ningún sistema digital es completamente infalible. Por ello, el usuario debe proteger sus credenciales, cerrar sesión en dispositivos compartidos, mantener actualizado su correo de recuperación y reportar accesos no reconocidos.</p>
    </section>

    <section>
      <h2>9. Derechos del titular</h2>
      <p>El titular puede solicitar acceso, rectificación, cancelación u oposición respecto de sus datos personales, así como revocar su consentimiento o limitar el uso o divulgación de su información, conforme a la legislación mexicana aplicable.</p>
      <p>La solicitud debe realizarse mediante los medios de contacto disponibles en IRIS e indicar el derecho que desea ejercer, datos para identificar la cuenta, descripción clara de la solicitud y documentos que acrediten identidad o representación cuando resulte necesario. IRIS responderá conforme a los plazos legales aplicables.</p>
    </section>

    <section>
      <h2>10. Menores de edad y representación</h2>
      <p>El uso de IRIS por menores de edad requiere intervención, autorización y supervisión de madre, padre, tutor o representante legal, según corresponda. IRIS podrá solicitar verificaciones adicionales y restringir funciones cuando no exista autorización suficiente o cuando el servicio no sea adecuado para la edad o condición del usuario.</p>
    </section>

    <section>
      <h2>11. Cookies y tecnologías similares</h2>
      <p>IRIS puede utilizar cookies, almacenamiento local, identificadores de sesión y tecnologías similares para mantener la sesión iniciada, recordar preferencias, mejorar seguridad, medir funcionamiento y analizar el uso de la plataforma. El detalle se encuentra en la Política de Cookies.</p>
    </section>

    <section>
      <h2>12. Cambios al aviso</h2>
      <p>IRIS puede modificar este Aviso de Privacidad para reflejar cambios legales, operativos, técnicos o de servicio. Las modificaciones estarán disponibles dentro de la plataforma y podrán notificarse por medios electrónicos cuando resulten relevantes.</p>
    </section>

    <section>
      <h2>13. Aceptación</h2>
      <p>Al marcar la casilla correspondiente en el formulario, crear una cuenta, usar funciones que impliquen tratamiento de datos o continuar utilizando IRIS después de la publicación de este aviso, el usuario manifiesta haber leído y comprendido este Aviso de Privacidad. El tratamiento de datos sensibles requiere la aceptación expresa del consentimiento correspondiente.</p>
