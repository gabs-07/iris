    <section>
      <h2>1. IRIS no es un servicio de emergencia</h2>
      <p>IRIS no presta servicios de ambulancia, urgencias, hospitalización, rescate, intervención policial, vigilancia permanente ni atención médica inmediata. La plataforma puede facilitar comunicación y orientación, pero no sustituye atención presencial de emergencia.</p>
    </section>

    <section>
      <h2>2. Cuándo buscar ayuda inmediata</h2>
      <p>El usuario debe buscar ayuda inmediata si presenta riesgo de suicidio, intención de autolesión, violencia, abuso, amenaza contra terceros, crisis severa, intoxicación, pérdida de control, síntomas médicos urgentes, alteración grave de conciencia o cualquier situación que pueda comprometer su vida, salud o integridad.</p>
    </section>

    <section>
      <h2>3. Medidas recomendadas</h2>
      <p>En una emergencia, el usuario debe comunicarse con los servicios de emergencia disponibles en su localidad, acudir al hospital o centro de salud más cercano, contactar a una persona de confianza, avisar a su contacto de emergencia o solicitar apoyo presencial inmediato.</p>
    </section>

    <section>
      <h2>4. Contacto de emergencia</h2>
      <p>Cuando el usuario proporcione un contacto de emergencia, autoriza que IRIS o el profesional tratante puedan utilizarlo cuando exista peligro razonable para la vida, salud o integridad del usuario o de terceros, o cuando sea necesario activar medidas de protección.</p>
    </section>

    <section>
      <h2>5. Limitaciones de la atención en línea</h2>
      <p>La atención en línea puede ser insuficiente para crisis graves, condiciones que requieran observación presencial, intervención médica, medicación urgente, hospitalización, contención física o coordinación inmediata con autoridades. El profesional podrá recomendar canalización, suspensión de la atención en línea o derivación a servicios especializados.</p>
    </section>

