    <section>
      <h2>1. Alcance</h2>
      <p>Estas condiciones aplican a psicólogos, terapeutas, orientadores, profesionales de salud mental o prestadores de servicios que utilicen IRIS para ofrecer atención, administrar agenda, recibir pagos, comunicarse con pacientes o llevar seguimiento.</p>
    </section>

    <section>
      <h2>2. Información profesional</h2>
      <p>El profesional debe proporcionar información veraz, vigente y comprobable sobre identidad, cédula profesional, formación, especialidad, experiencia, modalidad, horarios, tarifas y alcance de sus servicios. IRIS puede solicitar documentos de verificación y suspender perfiles cuando existan inconsistencias o riesgos.</p>
    </section>

    <section>
      <h2>3. Obligaciones profesionales</h2>
      <p>El profesional debe actuar con diligencia, ética, confidencialidad, respeto, no discriminación, competencia profesional y apego a la legislación aplicable. Debe obtener consentimiento informado cuando corresponda, mantener registros necesarios, proteger datos personales, informar límites del servicio y canalizar al paciente cuando el caso exceda su competencia o requiera atención presencial, médica, psiquiátrica o de emergencia.</p>
    </section>

    <section>
      <h2>4. Manejo de información</h2>
      <p>El profesional sólo podrá acceder a la información necesaria para atender al paciente y no deberá descargar, compartir, publicar, vender, reutilizar o divulgar datos personales fuera de las finalidades autorizadas. La información clínica y sensible debe tratarse con estricta confidencialidad.</p>
    </section>

    <section>
      <h2>5. Relación con pacientes</h2>
      <p>El profesional es responsable de la calidad, pertinencia y seguridad de la atención que presta. Debe respetar horarios, avisar cancelaciones, evitar conflictos de interés, documentar lo necesario, no prometer resultados garantizados y mantener límites profesionales adecuados.</p>
    </section>

    <section>
      <h2>6. Suspensión o retiro</h2>
      <p>IRIS puede suspender, limitar o retirar el perfil de un profesional por quejas graves, incumplimiento de estas condiciones, información falsa, riesgos para usuarios, uso indebido de datos, conducta antiética, falta de documentación, requerimiento de autoridad o cualquier situación que afecte la confianza y seguridad de la plataforma.</p>
    </section>

