    <section>
      <h2>1. Aceptación informada</h2>
      <p>Al marcar la casilla correspondiente antes de iniciar la atención psicológica en IRIS, el paciente manifiesta que ha recibido información clara sobre la naturaleza del servicio, sus beneficios esperados, límites, riesgos, alternativas, confidencialidad y condiciones de uso, y acepta participar voluntariamente en el proceso de atención psicológica a través de la plataforma.</p>
    </section>

    <section>
      <h2>2. Naturaleza del servicio</h2>
      <p>La atención puede consistir en orientación, evaluación, acompañamiento, intervención psicológica, psicoterapia, psicoeducación, seguimiento, tareas terapéuticas y comunicación con el profesional seleccionado. La modalidad puede ser en línea mediante videollamada, mensajería, agenda, formularios, diario o herramientas digitales disponibles en IRIS.</p>
      <p>El profesional tratante es responsable del criterio clínico, método de intervención, manejo terapéutico, registro profesional y decisiones de canalización. IRIS facilita la tecnología para la gestión del servicio y resguarda la información conforme a su Aviso de Privacidad.</p>
    </section>

    <section>
      <h2>3. Beneficios esperados</h2>
      <p>La atención psicológica puede ayudar a comprender emociones, desarrollar habilidades, mejorar hábitos, manejar síntomas, afrontar estrés, tomar decisiones, fortalecer relaciones y favorecer el bienestar. Los resultados no son automáticos ni garantizados, y dependen de la situación del paciente, continuidad, participación, contexto y criterio profesional.</p>
    </section>

    <section>
      <h2>4. Riesgos y limitaciones</h2>
      <p>Durante el proceso pueden surgir emociones intensas, incomodidad, recuerdos difíciles, desacuerdos, cambios en relaciones personales o necesidad de atención especializada. La atención en línea también puede presentar fallas de conexión, interrupciones, pérdida de privacidad en el entorno físico del usuario, limitaciones para observar lenguaje corporal o imposibilidad de actuar físicamente ante una emergencia.</p>
    </section>

    <section>
      <h2>5. Confidencialidad</h2>
      <p>La información compartida en el proceso será tratada con confidencialidad. La confidencialidad puede limitarse cuando exista riesgo para la vida, salud o integridad del paciente o de terceros; sospecha de violencia, abuso o delito que deba informarse; requerimiento de autoridad competente; autorización del paciente; supervisión profesional bajo confidencialidad; o cualquier otro supuesto permitido o exigido por la ley.</p>
    </section>

    <section>
      <h2>6. Emergencias y crisis</h2>
      <p>IRIS no es un servicio de emergencia. Si el paciente presenta riesgo inmediato, ideación suicida con plan, autolesión, violencia, intoxicación, crisis severa o cualquier situación que comprometa su seguridad, debe contactar servicios de emergencia, acudir al hospital más cercano, comunicarse con su contacto de emergencia o solicitar ayuda presencial inmediata.</p>
      <p>El paciente acepta que, ante riesgo razonable, el profesional o IRIS puedan contactar a una persona de emergencia, institución de salud, autoridad competente o servicio de auxilio para proteger la vida, salud o integridad de una persona.</p>
    </section>

    <section>
      <h2>7. Responsabilidades del paciente</h2>
      <p>El paciente debe proporcionar información veraz y completa, asistir puntualmente a sus citas, realizar pagos aplicables, mantener un espacio privado para sesiones en línea, informar cambios relevantes de salud, seguir indicaciones razonables del profesional, no grabar sesiones sin autorización y solicitar ayuda inmediata si se encuentra en riesgo.</p>
    </section>

    <section>
      <h2>8. Alternativas</h2>
      <p>El paciente puede optar por atención psicológica presencial, servicios médicos, psiquiatría, instituciones públicas o privadas de salud, líneas de apoyo emocional, servicios de emergencia o suspender el proceso cuando lo considere pertinente, sin perjuicio de obligaciones de pago o cancelación previamente aceptadas.</p>
    </section>

    <section>
      <h2>9. Retiro del consentimiento</h2>
      <p>El paciente puede retirar este consentimiento y suspender el uso de los servicios, considerando que la revocación no afecta servicios ya prestados, obligaciones pendientes, conservación de registros necesarios, pagos realizados o actuaciones indispensables para proteger la vida, salud o integridad de una persona.</p>
    </section>

    <section>
      <h2>10. Registro electrónico</h2>
      <p>La aceptación de este consentimiento puede registrarse electrónicamente con fecha, hora, cuenta, versión del documento, dirección IP, dispositivo, navegador y demás datos técnicos necesarios para acreditar la aceptación informada.</p>
    </section>

