    <section>
      <h2>1. Objeto</h2>
      <p>Los presentes Términos y Condiciones regulan el acceso y uso de IRIS, plataforma digital que permite registro de usuarios, búsqueda de profesionales, gestión de citas, pagos, videollamadas, comunicación, diario personal, tareas, seguimiento, comunidad y herramientas relacionadas con bienestar y atención psicológica.</p>
      <p>Al marcar la casilla de aceptación, crear una cuenta, reservar una cita, realizar un pago, participar en una sesión o utilizar cualquier módulo de IRIS, el usuario acepta obligarse por estos Términos y por los avisos, políticas y consentimientos aplicables.</p>
    </section>

    <section>
      <h2>2. Naturaleza de IRIS</h2>
      <p>IRIS opera como plataforma tecnológica para facilitar la administración y conexión entre usuarios y profesionales de la salud mental. La prestación directa de servicios psicológicos corresponde al profesional que atiende al paciente, conforme a su formación, cédula profesional, experiencia, ética profesional y obligaciones aplicables.</p>
      <p>IRIS no sustituye servicios de urgencia, hospitalización, intervención de crisis, atención médica presencial ni líneas oficiales de emergencia. Si el usuario se encuentra en peligro inmediato, debe contactar servicios de emergencia, acudir a una institución de salud o comunicarse con una persona de confianza.</p>
    </section>

    <section>
      <h2>3. Registro y cuenta</h2>
      <p>El usuario debe proporcionar información veraz, completa, actualizada y lícita. La cuenta es personal e intransferible. El usuario es responsable de conservar la confidencialidad de sus credenciales y de toda actividad realizada desde su cuenta, salvo que demuestre uso no autorizado no imputable a su conducta.</p>
      <p>IRIS puede suspender, limitar o cancelar cuentas cuando detecte información falsa, uso indebido, riesgo de seguridad, incumplimiento legal, afectación a otros usuarios, fraude, abuso, suplantación o cualquier conducta contraria a estos Términos.</p>
    </section>

    <section>
      <h2>4. Uso permitido</h2>
      <p>El usuario debe utilizar IRIS únicamente para fines personales, profesionales o administrativos lícitos relacionados con el objeto de la plataforma. Queda prohibido usar IRIS para acosar, amenazar, discriminar, difamar, suplantar identidad, obtener datos sin autorización, distribuir malware, vulnerar seguridad, interferir con el servicio, publicar contenido ilegal, promocionar productos no autorizados o realizar actividades contrarias a la ley o a la ética profesional.</p>
    </section>

    <section>
      <h2>5. Atención psicológica</h2>
      <p>La atención psicológica puede incluir orientación, evaluación, acompañamiento, intervención, psicoterapia, seguimiento, tareas y psicoeducación. Los resultados no están garantizados y dependen de múltiples factores, incluyendo continuidad, participación, contexto personal, gravedad del motivo de consulta y criterio profesional.</p>
      <p>El paciente debe proporcionar información suficiente y veraz al profesional, informar situaciones de riesgo, comunicar cambios relevantes de salud y solicitar ayuda inmediata en caso de crisis. El profesional determinará, conforme a su criterio, si el caso requiere canalización, atención presencial, intervención médica, apoyo psiquiátrico o servicios de emergencia.</p>
    </section>

    <section>
      <h2>6. Profesionales registrados</h2>
      <p>Los profesionales deben proporcionar información real sobre identidad, cédula, experiencia, especialidad, disponibilidad, honorarios y forma de atención. Deben actuar conforme a la legislación aplicable, normas profesionales, confidencialidad, consentimiento informado, expediente o registros clínicos, protección de datos personales y deberes éticos.</p>
      <p>IRIS puede revisar documentación, solicitar aclaraciones, limitar perfiles, suspender servicios o retirar profesionales cuando existan inconsistencias, quejas, incumplimientos, riesgos para usuarios o requerimientos legales.</p>
    </section>

    <section>
      <h2>7. Pagos, cargos y comprobantes</h2>
      <p>El usuario acepta pagar los precios, honorarios, comisiones, cargos, impuestos y conceptos mostrados antes de confirmar una cita, compra o suscripción. Los pagos pueden procesarse mediante terceros autorizados. IRIS no debe solicitar contraseñas bancarias ni datos de autenticación ajenos al flujo seguro del procesador de pago.</p>
      <p>Los comprobantes, facturas o recibos se emitirán conforme a las funciones disponibles, la información proporcionada por el usuario y las obligaciones fiscales aplicables.</p>
    </section>

    <section>
      <h2>8. Cancelaciones, reembolsos y cambios</h2>
      <p>Las cancelaciones, reprogramaciones, inasistencias, retrasos, reembolsos y controversias de pago se rigen por la Política de Cancelaciones y Reembolsos publicada por IRIS y por las condiciones visibles al momento de contratar el servicio.</p>
    </section>

    <section>
      <h2>9. Comunidad y contenido de usuarios</h2>
      <p>Cuando IRIS permita publicar contenido en comunidad, comentarios o espacios compartidos, el usuario conserva responsabilidad por lo que publique. IRIS puede moderar, ocultar, eliminar o reportar contenido que vulnere derechos, privacidad, seguridad, normas de convivencia o disposiciones legales.</p>
      <p>No se permite publicar datos clínicos de terceros, información de menores, datos de contacto privados, diagnósticos de otras personas, contenido violento, discriminatorio, sexual explícito, fraudulento, comercial no autorizado o que promueva autolesión.</p>
    </section>

    <section>
      <h2>10. Propiedad intelectual</h2>
      <p>La marca IRIS, diseño, interfaces, textos, estructura, software, logotipos, bases de datos, contenidos propios y elementos de la plataforma pertenecen a IRIS o a sus titulares autorizados. El usuario no adquiere derechos de propiedad por usar la plataforma.</p>
      <p>El contenido creado por usuarios seguirá perteneciendo a sus autores, pero el usuario autoriza a IRIS a almacenarlo, procesarlo, mostrarlo, transmitirlo o eliminarlo según sea necesario para operar el servicio, cumplir estos Términos y atender obligaciones legales.</p>
    </section>

    <section>
      <h2>11. Privacidad y datos personales</h2>
      <p>El tratamiento de datos personales se rige por el Aviso de Privacidad Integral. El uso de funciones relacionadas con atención psicológica, diario, tareas, expediente o seguimiento puede requerir aceptación expresa del Consentimiento para Datos Personales Sensibles y del Consentimiento Informado.</p>
    </section>

    <section>
      <h2>12. Disponibilidad del servicio</h2>
      <p>IRIS procurará mantener la plataforma disponible y segura, pero no garantiza funcionamiento ininterrumpido, libre de errores o compatible con todos los dispositivos. El servicio puede suspenderse por mantenimiento, actualización, fallas técnicas, ciberseguridad, fuerza mayor, instrucciones de autoridad o causas fuera del control razonable de IRIS.</p>
    </section>

    <section>
      <h2>13. Limitación de responsabilidad</h2>
      <p>IRIS no será responsable por daños derivados de información falsa proporcionada por usuarios, uso indebido de la cuenta, decisiones tomadas sin consultar al profesional correspondiente, fallas de conexión del usuario, indisponibilidad de terceros, incumplimientos de profesionales independientes o situaciones de emergencia no reportadas oportunamente.</p>
      <p>Lo anterior no limita derechos irrenunciables del consumidor ni responsabilidades que legalmente no puedan excluirse.</p>
    </section>

    <section>
      <h2>14. Terminación</h2>
      <p>El usuario puede dejar de utilizar IRIS en cualquier momento. La eliminación de cuenta o cancelación de servicios no elimina obligaciones pendientes, registros necesarios para cumplimiento legal, comprobantes, evidencias de consentimiento, información sujeta a conservación profesional o datos bloqueados por razones legales.</p>
    </section>

    <section>
      <h2>15. Modificaciones</h2>
      <p>IRIS puede actualizar estos Términos por cambios legales, técnicos, operativos o comerciales. Las versiones vigentes estarán disponibles en la plataforma. El uso continuo de IRIS después de una actualización implica aceptación de la versión vigente, salvo que la ley exija un consentimiento adicional.</p>
    </section>

    <section>
      <h2>16. Legislación aplicable</h2>
      <p>Estos Términos se interpretan conforme a las leyes aplicables en México. Las controversias se atenderán por los medios de soporte de IRIS y, cuando proceda, por las autoridades competentes.</p>
