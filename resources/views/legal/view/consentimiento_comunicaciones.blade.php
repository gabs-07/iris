      <h2>1. Alcance</h2>
      <p>Este consentimiento regula el envío de comunicaciones electrónicas no indispensables para la prestación directa del servicio, incluyendo frases motivacionales, contenido de bienestar, boletines, novedades, encuestas, promociones, recomendaciones de uso e información de IRIS.</p>
