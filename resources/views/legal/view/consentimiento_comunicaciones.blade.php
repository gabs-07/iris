    <section>
      <h2>2. Consentimiento</h2>
      <p>Al marcar la casilla opcional correspondiente, el usuario autoriza a IRIS a enviar comunicaciones electrónicas al correo, teléfono, cuenta, notificaciones del sistema u otros medios proporcionados por el usuario.</p>
      <p>La negativa a recibir comunicaciones promocionales o secundarias no impedirá crear cuenta ni usar servicios principales, salvo que se trate de mensajes indispensables como verificación de correo, recuperación de contraseña, confirmación de citas, avisos de seguridad, comprobantes de pago, cambios legales, soporte o información operativa necesaria.</p>
    </section>

    <section>
      <h2>3. Baja o revocación</h2>
      <p>El usuario puede cancelar su suscripción a comunicaciones secundarias desde el enlace de baja incluido en los correos, desde las preferencias de cuenta o mediante los medios de contacto disponibles en IRIS. La baja puede tardar un periodo razonable en reflejarse en todos los sistemas.</p>
    </section>

    <section>
      <h2>4. Comunicaciones necesarias</h2>
      <p>Aunque el usuario rechace comunicaciones secundarias, IRIS podrá enviar mensajes necesarios para operar la cuenta, validar identidad, confirmar citas, notificar pagos, informar cambios de seguridad, actualizar documentos legales, responder solicitudes o cumplir obligaciones legales.</p>
